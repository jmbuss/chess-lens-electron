/**
 * Convert a centipawn score to a win rate percentage (0-1).
 * Uses the Maia empirical lookup table for accurate conversion.
 *
 * Scores beyond the table range are clamped to the boundary values.
 * Mate scores are mapped to near-0 or near-1 win rates.
 */
export function cpToWinRate(centipawns: number): number {
  const pawns = Math.round(centipawns / 10) / 10

  if (pawns <= -10.0) return CP_LOOKUP[-10.0]
  if (pawns >= 10.0) return CP_LOOKUP[10.0]

  const rounded = Math.round(pawns * 10) / 10
  const winRate = CP_LOOKUP[rounded]

  if (winRate !== undefined) return winRate

  // Linear interpolation for values between table entries
  const lower = Math.floor(pawns * 10) / 10
  const upper = Math.ceil(pawns * 10) / 10
  const lowerWr = CP_LOOKUP[lower]
  const upperWr = CP_LOOKUP[upper]

  if (lowerWr === undefined || upperWr === undefined) {
    // Fallback: simple sigmoid approximation
    return 1 / (1 + Math.pow(10, -pawns / 4))
  }

  const t = (pawns - lower) / (upper - lower)
  return lowerWr + t * (upperWr - lowerWr)
}

/**
 * Convert a UCI score (cp or mate) to a win rate percentage (0-1).
 * From the perspective of the side to move.
 */
export function scoreToWinRate(type: 'cp' | 'mate', value: number): number {
  if (type === 'mate') {
    // Mate in N: near-certain win/loss
    if (value > 0) return 0.999
    if (value < 0) return 0.001
    return 0.5
  }

  return cpToWinRate(value)
}
